    def updateFunc(self):

        if svnDir is None:
            QtGui.QMessageBox.about(self, "Updating error", "No folder selected to update SVN!")
        else:
            path = svnDir
            if os.listdir(path) == []:
                QtGui.QMessageBox.about(self, "Updating", "No current content detected.\nFull download initiated.")
                srpsvn.checkout('http://svn.santosrp.com/svn/santosv2', 'content')
            else:
                try:
                    QtGui.QMessageBox.about(self, "Updating", "The update has started! \n \nThis program might go into not responding, please do not close this program, just minimize me!\n\nI'll let you know once the download is complete :) ")
                    srpsvn.update('content')
                except:
                    QtGui.QMessageBox.about(self, "Damnit", "Previous operation not finished!\nIniating cleanup....\n \nPlease press the update button again!")
                    srpsvn.cleanup(svnDir)

        QtGui.QMessageBox.about(self, "Completed", "Download completed!")

